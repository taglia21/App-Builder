def _validate_python(source: str) -> Optional[str]:
    """
    Attempt to parse *source* as Python.

    Returns ``None`` on success, or an error message string on failure.
    """
    try:
        ast.parse(source)
        return None
    except SyntaxError as exc:
        return f"SyntaxError at line {exc.lineno}: {exc.msg}"


def _validate_typescript(source: str) -> Optional[str]:
    """
    Perform lightweight heuristic checks on TypeScript/JavaScript source.

    Returns ``None`` when the source looks plausible, or an error message.
    """
    stripped = source.strip()
    if not stripped:
        return "File is empty"

    # Balanced braces check (handles basic structural issues)
    depth = 0
    in_string: Optional[str] = None
    prev = ""
    for ch in stripped:
        if in_string:
            if ch == in_string and prev != "\\":
                in_string = None
        else:
            if ch in ('"', "'", "`"):
                in_string = ch
            elif ch == "{":
                depth += 1
            elif ch == "}":
                depth -= 1
                if depth < 0:
                    return "Unmatched closing brace '}'"
        prev = ch

    if depth != 0:
        return f"Unbalanced braces: {depth} unclosed '{'{'}'"

    # Must contain at least one meaningful construct
    keywords = (
        "export", "import", "const", "let", "function",
        "class", "interface", "type ", "return",
    )
    if not any(kw in stripped for kw in keywords):
        return "File appears to contain no meaningful TypeScript/JavaScript constructs"

    return None


def _validate_file(relative_path: str, source: str) -> Optional[str]:
    """Dispatch to the correct validator based on file extension."""
    ext = Path(relative_path).suffix.lower()
    if ext == ".py":
        return _validate_python(source)
    if ext in (".ts", ".tsx", ".js", ".jsx"):
        return _validate_typescript(source)
    return None  # No validation for JSON, TOML, YAML, Dockerfile, etc.


